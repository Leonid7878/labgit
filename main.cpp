#include <iostream>

int main() {
    int num1, num2;

    // Ввод чисел
    std::cout << "Введите первое число: ";
    std::cin >> num1;
    std::cout << "Введите второе число: ";
    std::cin >> num2;

    // Вывод разности чисел
    std::cout << "Разность чисел: " << num1 - num2 << std::endl;

    return 0;
}
